/*
 * spring-boot-kotlin — Kotlin Multiplatform port of Spring Boot.
 *
 * Multi-module build structured after kotlinmania/km-io:
 *   - build-logic/ is an included build providing the ported Spring Boot
 *     convention plugins (org.springframework.boot.*).
 *   - The shared version catalog lives in gradle/libs.versions.toml.
 */

pluginManagement {
    includeBuild("build-logic")

    repositories {
        google()
        mavenCentral()
        gradlePluginPortal()
        maven { url = uri("https://repo.spring.io/snapshot") }
    }

    resolutionStrategy {
        eachPlugin {
            // Trailing dot is required: "org.jetbrains.kotlinx.benchmark" starts
            // with "org.jetbrains.kotlin" (kotlinx!), so a dotless prefix would
            // wrongly force kotlinx.* plugins to the Kotlin version.
            if (requested.id.id.startsWith("org.jetbrains.kotlin.")) {
                // Template's gradle.properties exposes the Kotlin version as
                // `versions.kotlin`; fall back rather than failing if absent.
                useVersion(providers.gradleProperty("versions.kotlin").getOrElse("2.4.0"))
            }
        }
    }
}

plugins {
    id("org.gradle.toolchains.foojay-resolver-convention") version "1.0.0"
    // Spring's ConventionsPlugin (applied per-module) reads a DevelocityTestConfiguration
    // extension registered by this settings plugin (ported from Spring's original
    // settings.gradle). It's an external plugin, so no build-logic classpath leak.
    id("io.spring.develocity.conventions") version "0.0.25"
}

// NOTE: Spring's cycle-detection is a Plugin<Settings>; it's registered in
// build-logic but intentionally NOT applied here. Applying any build-logic
// plugin at settings time leaks build-logic's kotlin-gradle-plugin onto the
// classpath, which then conflicts with the root build's versioned
// `kotlin.multiplatform` request ("already on classpath with unknown version").
// km-io applies no build-logic plugin at settings level; we follow suit.

dependencyResolutionManagement {
    repositories {
        google()
        mavenCentral()
        maven { url = uri("https://repo.spring.io/snapshot") }
    }
}

